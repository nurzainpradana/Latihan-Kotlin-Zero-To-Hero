fun main(){
    // Deklarasi variabel
    // Var -> Muttable -> Variabel yang nilainya bisa berubah
    var name = "Zain"

    // Mengganti value variable
    name = "Nur Zain"

    //Mencetak Variable
    println(name)

    // Val -> Immutable -> Variabel yang nilainya tidak bisa diubah
    val cardId = 23215123124
    // cardId = 242376547435 //Compilation ERROR
    print(cardId)
}